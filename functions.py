# Functions = um bloco de código que pode ser chamado várias vezes e assim executado.

def hello(name):
    print('Hello World! ' + name + "!")
    print("Have a nice day!")


hello("Bro")
hello("Dude")


def hello(name1, name2):
    print('Hello World! ' + name1 + " " + name2 + "!")


hello("Bro", "Code")
