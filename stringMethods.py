name = "Bro Code"

print(len(name))
print(name.find("B"))  # acha onde está o primeiro b
print(name.capitalize())  # primeira letra maiuscula
print(name.upper())  # todas maiusculas
print(name.lower())  # todas minusculas
print(name.isdigit())  # verifica se é um numero
print(name.isalpha())  # verifica se é alfabeto
print(name.count("o"))  # conta quantas vezes aparece
print(name.replace("Bro", "Sisters"))  # troca
print(name*30)  # multiplica por 30
